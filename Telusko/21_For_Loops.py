# range
for i in range(10):
    print(i)    #0 to 9

for i in range(11,21,1):
    print(i)    #11 to 20

for i in range(11,21,2):
    print(i)    #11,13,15,17,19

# reverse printing
for i in range(21,10,-1):
    print(i)    #21 to 11

# for loop in side if codition
for i in range(1,21):
    if (i %5 == 0):
        print(i)




