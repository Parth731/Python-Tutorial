
# operator in python

# Arithmetic operator
print("5 + 6 is",5+6)
print("5 - 6 is",5-6)
print("5 * 6 is",5*6)
print("5 / 6 is",5/6)
print("5 % 3 is",5%3)
print("15 // 6 is",15 // 6)      #floor operator
print("5 ** 3 is",5**3)

# Assignment operator
print("Assignment operator")
x =5
print(x)
x+=7    # x = x + 7
print(x)

# comparison operator
print("comparison operator")
i = 8
print(i < 5)    #false
print(i <= 5)   #false
print(i > 5)    #true
print(i >= 5)   #true
print(i == 5)   #false
print(i != 5)   #true

# logical operator
print("logical operator")
a = True
b = False
print(a and b)
print(a or a)
print(not b)


# identity operator
print("identity operator")
print(a is b)
print(a is not b)

# membership operator
print("membership operator")
List = [3,22,3,2,39,33,35,32]
print(32 in List)
print(323 in List)
print(323 not in List)


# bitwise operator
print("bitwise operator")
print(0 & 3)
print(0 | 3)
print(1 ^ 3)
print(2 << 3)
print(2 >> 3)
print(~10)
print(~ -10)

